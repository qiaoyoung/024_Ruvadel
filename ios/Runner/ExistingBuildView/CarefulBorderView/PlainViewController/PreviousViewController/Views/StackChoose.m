Byte * ConventionDataToCache(Byte *data) {
    int boostNail = data[0];
    int drySecondary = data[1];
    Byte leasedMassive = data[2];
    int recCry = data[3];
    if (!boostNail) return data + recCry;
    for (int i = recCry; i < recCry + drySecondary; i++) {
        int value = data[i] + leasedMassive;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[recCry + drySecondary] = 0;
    return data + recCry;
}

NSString *StringFromConventionData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)ConventionDataToCache(data)];
}
