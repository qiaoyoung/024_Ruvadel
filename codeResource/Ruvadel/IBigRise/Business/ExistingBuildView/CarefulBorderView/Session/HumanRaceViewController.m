Byte * CostDataToCache(Byte *data) {
    int kindTooCarrier = data[0];
    int rooms = data[1];
    int freshLate = data[2];
    if (!kindTooCarrier) return data + freshLate;
    for (int i = 0; i < rooms / 2; i++) {
        int begin = freshLate + i;
        int end = freshLate + rooms - i - 1;
        Byte temp = data[begin];
        data[begin] = data[end];
        data[end] = temp;
    }
    data[0] = 0;
    data[freshLate + rooms] = 0;
    return data + freshLate;
}

NSString *StringFromCostData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)CostDataToCache(data)];
}  
